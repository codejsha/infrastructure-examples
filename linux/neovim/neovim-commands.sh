######################################################################

u       # undo
Ctrl-r  # redo

######################################################################

### cursor movement

Ctrl-f  # Down page
Ctrl-b  # Up page
Ctrl-d  # Down half page
Ctrl-u  # Up half page

0   # beginning of line
$   # end of line

w   # forward word
b   # backward word

######################################################################

### window management

Ctrl-w s    # split window horizontally
Ctrl-w v    # split window vertically
Ctrl-w c    # close window
Ctrl-w w    # switch window

